def get_time_format():
    now = datetime.datetime.now()

    day = now.day
    month = now.month
    if int(month) < 10:
        month = f"0{month}"

    hour = now.hour
    min = now.minute
    if int(min) < 10:
        min = f"0{min}"

    return f"{day}/{month} {hour}:{min}"


